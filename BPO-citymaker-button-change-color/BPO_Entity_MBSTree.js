//已开工,已完成
var m_color;
function getData(type,color){
m_color=color;
var sql="";
if(type=="F")
{
sql="DATEDIFF (day, ActualFinish, GETDATE()) >0 and WBS1 like'%.%.%'";
}
else if(type=="Delay")
{
//计划已开工，但实际未开工
sql="DATEDIFF (day, Start, GETDATE()) >0 and CONVERT(varchar(100), ActualStart, 23)='1900-01-01' and WBS1 like'%.%.%'";
}
else if(type=="ScheduleNS")
{
sql="DATEDIFF (day, Start, GETDATE()) <0 and WBS1 like'%.%.%'";
}
else if(type=="ScheduleS")
{
sql="DATEDIFF (day, Start, GETDATE()) >0 and WBS1 like'%.%.%'";
}
else if(type=="ActualNS")
{
sql="CONVERT(varchar(100), ActualStart, 23)='1900-01-01' and WBS1 like'%.%.%'";
}
else if(type=="ActualS")
{
sql="CONVERT(varchar(100), ActualStart, 23)!='1900-01-01' and WBS1 like'%.%.%'";
}

BC_QueryWBSBase.SetCondiList(null,null,null,sql,0);
BC_QueryWBSBase.getData();
BC_QueryWBSBase.on("AfterGetData",cbWBS2);
};

